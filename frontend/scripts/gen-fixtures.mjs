/**
 * Regenerates src/mocks/fixtures/generated.ts from ../fixtures/data/*.jsonl
 * (the executable form of docs/contract.md). The output is COMMITTED so the
 * subtree-pushed Lovable repo is self-contained without the parent monorepo.
 *
 * Run: npm run gen:fixtures
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const FIXTURES = path.resolve(__dirname, "../../fixtures/data");
const OUT = path.resolve(__dirname, "../src/mocks/fixtures/generated.ts");

const read = (name) =>
  readFileSync(path.join(FIXTURES, `${name}.jsonl`), "utf8")
    .trim()
    .split("\n")
    .filter(Boolean)
    .map((line) => JSON.parse(line));

const idealCandidates = read("gold.ideal_candidate").map((row) => {
  const { embedding, ...rest } = row; // 1024-dim vector — useless in the UI, huge in the bundle
  return rest;
});

const tables = {
  ventures: read("gold.venture"),
  ventureScores: read("gold.venture_score"),
  ventureMembers: read("gold.venture_member"),
  theses: read("gold.thesis"),
  scoreWeights: read("gold.score_weights"),
  outreach: read("gold.outreach"),
  ventureGaps: read("gold.venture_gaps"),
  interviews: read("gold.interview"),
  memos: read("gold.memo"),
  candidatePool: read("gold.candidate_pool"),
  idealCandidates,
  persons: read("silver.person"),
  // Admin/database-overview tables (read-only viz)
  personConnections: read("silver.person_connection"),
  personSourceRecords: read("silver.person_source_record"),
  personSourceLinks: read("silver.person_source_link"),
  authorships: read("silver.authorship"),
  contributions: read("silver.contribution"),
  officers: read("silver.officer"),
  publications: read("silver.publication"),
  projects: read("silver.project"),
  companies: read("silver.company"),
  personFeatures: read("gold.person_features"),
  erReviewQueue: read("ops.er_review_queue"),
};

const body = Object.entries(tables)
  .map(([key, rows]) => `export const ${key} = ${JSON.stringify(rows, null, 2)};`)
  .join("\n\n");

const header = `/* AUTO-GENERATED by scripts/gen-fixtures.mjs from fixtures/data/*.jsonl. Do not edit.
 * Committed so the app is self-contained when subtree-pushed to the Lovable repo. */
/* eslint-disable */

`;

mkdirSync(path.dirname(OUT), { recursive: true });
writeFileSync(OUT, header + body + "\n");
console.log(`Wrote ${OUT}`);
for (const [key, rows] of Object.entries(tables)) console.log(`  ${key}: ${rows.length} rows`);
